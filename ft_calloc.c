#include "libft.h"

void	*ft_calloc(size_t count, size_t size)
{
	char	*str;
	int		i;

	i = -1;
	str = (char *) malloc (count * size);
	if (!str)
		return (0);
	while (++i < (int) count * (int) size)
		str[i] = 0;
	return (str);
}
